#Award program
while True:
    b_num = int(input("Enter number of B grades attained this year: "))

    if b_num == 2:
        print("You get a participation award.")
    elif b_num == 3:
        print("You get a credit award.")
    elif b_num == 4:
        print("You get a distinction award.")
    elif b_num == 5:
        print("You get a distinction award.")
    elif b_num == 6:
        print("You get a high distinction award.")
    else:
        print("You don't get an award.")
